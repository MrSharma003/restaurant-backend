class Restaurant:
    def __init__(self, name: str, city: str, area: str, cuisine: str, rating: float, cost_for_two: float, is_veg: bool):
        self.name = name
        self.city = city
        self.area = area
        self.cuisine = cuisine
        self.rating = rating
        self.cost_for_two = cost_for_two
        self.is_veg = is_veg
